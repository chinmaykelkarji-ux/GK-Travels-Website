export const getTourBySlug = (slug: string) => tours.find((t) => t.slug === slug);

export const getToursByDestination = (destinationSlug: string) =>
  tours.filter((t) => t.destinationSlug === destinationSlug);

export const getToursByCategory = (category: Tour["category"]) =>
  tours.filter((t) => t.category === category);

export const getFeaturedTours = () => tours.filter((t) => t.featured);

export const getRelatedTours = (tour: Tour, limit = 3) =>
  tours
    .filter((t) => t.slug !== tour.slug && t.category === tour.category)
    .slice(0, limit);
